// Helper function to get symptom name
export const getSymptomName = (symptomKey, language = 'en') => {
  const symptom = symptomTranslations[symptomKey];
  return symptom ? symptom[language] : symptomKey.split('_').map(word => 
    word.charAt(0).toUpperCase() + word.slice(1)
  ).join(' ');
};

// Helper function to get disease name
export const getDiseaseName = (diseaseKey, language = 'en') => {
  const disease = diseaseTranslations[diseaseKey];
  return disease ? disease[language] : diseaseKey.split('_').map(word => 
    word.charAt(0).toUpperCase() + word.slice(1)
  ).join(' ');
};